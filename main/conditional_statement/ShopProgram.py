# Условные операторы: задание 8

"""
Программа “Магазин”, выводим меню из трех товаров с ценами.
Пользователь выбирает товар, вводит сумму денег.
Если сумма меньше цены, выводим сообщение: “Денег не хватает!”,
если сумма равна цене – “Спасибо за покупку!”, если больше – “Ваша сдача: [сумма сдачи]”
"""

products = ["Товар 1", "Товар 2", "Товар 3"]
prices = [10.0, 20.0, 30.0]

for i in range(len(products)):
    print(products[i] + " = " + str(prices[i]) + " руб.")

choice = int(input("Выберите товар (введите номер): "))

if choice < 1 or choice > len(products):
    print("Такого товара нет!")
else:
    price = prices[choice - 1]
    sum_ = float(input("Введите сумму денег: "))

    if sum_ < price:
        print("Денег не хватает!")
    elif sum_ == price:
        print("Спасибо за покупку!")
    else:
        change = sum_ - price
        print("Ваша сдача: " + str(change) + " руб.")
